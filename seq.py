shoplist = ['apple','mango','carrot','banana']

#indexing of 'subcription' operation
print ('Itme 0 is',shoplist[0])
print ('Itme 1 is',shoplist[1])
print ('Itme 2 is',shoplist[2])
print ('Itme 3 is',shoplist[3])
print ('Itme -1 is',shoplist[-1])
print ('Itme -2 is',shoplist[-2])

#Slicing on a list
print ('Item 1 to 4 is',shoplist[1:4])
print ('Item 2 to end is',shoplist[2:])
print ('Item 1 to -1 is',shoplist[1:-1])
print ('Item start to end is',shoplist[:])

#Slicing on a string
name = 'swaroop'
print ('characters 1 to 3 is',name[1:3])
print ('characters 2 to end is',name[2:])
print ('characters start to -1 is',name[:-1])
print ('characters start to end is',name[:])
